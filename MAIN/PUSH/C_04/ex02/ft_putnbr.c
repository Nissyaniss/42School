#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
	}
	else
	{
		if (nb > 9)
			ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + 48);
	}
}

int	main(void)
{
	puts("int max = ");
	ft_putnbr(2147483647);
	puts("\n");
	puts("int min = ");
	ft_putnbr(-2147483648);
	puts("\n");
	puts("Zero = ");
	ft_putnbr(0);
	puts(" ");
	return (0);
}
