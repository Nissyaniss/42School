#include <unistd.h>

void	print(int tab[4][4])
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (n < 4)
	{
		while (i < 4)
		{
			tab[n][i] += '0';
			write(1, &tab[n][i], 1);
			if (i < 4 - 1)
				write(1, " ", 1);
			i++;
		}
		write(1, "\n", 1);
		n++;
		i = 0;
	}
}
